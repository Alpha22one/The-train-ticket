package tiketKereta;

public class Tiket {
    private int harga;
    
    public Tiket(int harga) {
        this.harga = harga;
    }

    public int getHarga() {
        return harga;
    }
    
     public int hartik (int orang){
     if(orang>=3){
        return harga=(harga*8/10)*orang;
     }
     else{
        return harga=harga*orang;
     }
     }
}
